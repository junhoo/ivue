<template>
  <button class="yi-button" :class="{[`icon-${iconPosition}`]: true}"
    @click="$emit('click')">
    <yi-icon class="icon" v-if="icon && !loading" :name="icon"></yi-icon>
    <yi-icon class="loading icon" v-if="loading" name="loading"></yi-icon>
    <div class="yi-button-content">
      <slot></slot>
    </div>
  </button>
</template>
<script>
  import Icon from './icon'
  export default {
    name: 'YiButton',
    components: {
      'yi-icon': Icon
    },
    props: {
      icon: {},
      loading: {
        type: Boolean,
        default: false
      },
      iconPosition: {
        type: String,
        default: 'left',
        validator (value) {
          return value === 'left' || value === 'right'
        }
      }
    }
  }
</script>
<style lang="scss" scoped>
  @import "variables";
  @keyframes spin {
    0% { transform: rotate(0deg); }
    100% { transform: rotate(360deg); }
  }
